import React from 'react';
import PropTypes from 'prop-types';
import Button from '@material-ui/core/Button';
import ClickAwayListener from '@material-ui/core/ClickAwayListener';
import Grow from '@material-ui/core/Grow';
import Paper from '@material-ui/core/Paper';
import Popper from '@material-ui/core/Popper';
import MenuItem from '@material-ui/core/MenuItem';
import MenuList from '@material-ui/core/MenuList';
import ListItemIcon from '@material-ui/core/ListItemIcon';
import ListItemText from '@material-ui/core/ListItemText';
import DeleteIcon from '@material-ui/icons/Delete';

import { withStyles } from '@material-ui/core/styles';

const styles = theme => ({
  root: {
    display: 'flex',
  },
  paper: {
    marginRight: theme.spacing.unit * 2,
  },
});

class Menu_dropdown extends React.Component {
  state = {
    open: false,
  };

  handleToggle = () => {
    this.setState(state => ({ open: !state.open }));
  };

  handleClose = event => {
    if (this.anchorEl.contains(event.target)) {
      return;
    }

    this.setState({ open: false });
  };

  render() {
    const { classes, Delete } = this.props;
    const { open } = this.state;

    return (
      <div className={classes.root}>
        
        <div>
          <Button
            buttonRef={node => {
              this.anchorEl = node;
            }}
            aria-owns={open ? 'menu-list-grow' : undefined}
            aria-haspopup="true"
            onClick={this.handleToggle}
          >
            
            <i className="material-icons">more_horiz</i>
          </Button>
          <Popper open={open} anchorEl={this.anchorEl} transition disablePortal>
            {({ TransitionProps, placement }) => (
              <Grow
                {...TransitionProps}
                id="menu-list-grow"
                style={{ transformOrigin: placement === 'bottom' ? 'center top' : 'center bottom' }}
              >
                <Paper>
                  <ClickAwayListener onClickAway={this.handleClose}>
                    <MenuList>
                            <MenuItem  className={classes.menuItem}>
                                <ListItemIcon className={classes.icon}>
                                <i className="material-icons">redo</i>
                                </ListItemIcon>
                                <ListItemText classes={{ primary: classes.primary }} inset primary="Share" />
                            </MenuItem>
                                
                            <MenuItem onClick={Delete} className={classes.menuItem}>
                                <ListItemIcon className={classes.icon}>
                                <DeleteIcon className={classes.icon} />

                                </ListItemIcon>
                                <ListItemText classes={{ primary: classes.primary }} inset primary="Delete" />
                            </MenuItem>
                    </MenuList>
                  </ClickAwayListener>
                </Paper>
              </Grow>
            )}
          </Popper>
        </div>
      </div>
    );
  }
}

Menu_dropdown.propTypes = {
  classes: PropTypes.object.isRequired,
};

export default withStyles(styles)(Menu_dropdown);